package Associazioni;

public class Alloggio extends Luogo{
	
	private int codal;
	private int bagni;
	private int camere;
	private int metriq;
	
	
	public int getCodal() {
		return codal;
	}
	public void setCodal(int codal) {
		this.codal = codal;
	}
	public int getBagni() {
		return bagni;
	}
	public void setBagni(int bagni) {
		this.bagni = bagni;
	}
	public int getCamere() {
		return camere;
	}
	public void setCamere(int camere) {
		this.camere = camere;
	}
	public int getMetriq() {
		return metriq;
	}
	public void setMetriq(int metriq) {
		this.metriq = metriq;
	}

}
